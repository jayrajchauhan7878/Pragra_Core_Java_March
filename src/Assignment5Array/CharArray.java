package Assignment5Array;

public class CharArray {

    public static void main(String[] args) {

        char[] ch = {'A','B','C','D'};

       for (int i = 0; i < ch.length; i++) {

            System.out.println(ch[i]+" ");

        }
        for ( char elements : ch){

            System.out.println(elements);

        }
    }





}
