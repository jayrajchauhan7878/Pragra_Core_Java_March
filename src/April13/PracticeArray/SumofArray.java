package April13.PracticeArray;

public class SumofArray {

    public static void main(String[] args) {

        int[] array = {23,44,23,52,23,87,87};
        int sum = 0;

        for (int i = 0; i < array.length; i++) {


            sum = sum + array[i];





        }
        System.out.println(sum);
    }
}
