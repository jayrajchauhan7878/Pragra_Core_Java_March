package Assignment3;

public class SumOf10Number {

   // write java program to find the sum of first 10 natural numbers

    public static void main(String[] args) {

        int sum = 0;
        for(int i = 1 ; i <= 10; i++){



            sum = sum + i;
            System.out.println(i);

        } System.out.println("Sum of 10 number is "+ sum);
    }
}
